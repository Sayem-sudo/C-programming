// this code about While Loop which is same as For loop

#include <stdio.h>
int main()
{
    /*first = Initialization
    second= while (condition)
     third= pore increment/decrement*/
    int i;
    i = 1;
    while (i <= 5)
    {
        printf("%d\n", i);
        i = i + 1;
    }

    return 0;
}