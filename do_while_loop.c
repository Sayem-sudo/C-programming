// This code about Do While Loop.
// Here significant is initial value.Akbar kaj korbe.
#include <stdio.h>
int main()
{
    int i;
    //  i = 100; 100 print korbe
    i = 1;
    do
    {
        printf("%d\n", i);
        i = i + 1;
    } while (i <= 5); // Always while ar moddhe condition

    return 0;
}